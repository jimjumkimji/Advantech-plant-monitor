@asynccontextmanager
async def lifespan(app: FastAPI):
    print("ğŸš€ Starting background Dropbox sensor sync...")

    from backend.dropbox import service as dropbox_service

    stop_flag = {"stop": False}

    def sync_loop():
        while not stop_flag["stop"]:
            try:
                dropbox_service.refresh_sensor_cache(
                    limit=1000,        # à¹€à¸à¹‡à¸šà¸‚à¹‰à¸­à¸¡à¸¹à¸¥à¸¥à¹ˆà¸²à¸ªà¸¸à¸” 1,000 à¹à¸–à¸§
                    interval="5min"    # aggregate à¸£à¸²à¸¢ 5 à¸™à¸²à¸—à¸µ
                )
            except Exception as e:
                print(f"âš ï¸ Error refreshing sensor cache: {e}")

            time.sleep(60)  # Sync à¸—à¸¸à¸ 60 à¸§à¸´à¸™à¸²à¸—à¸µ

    thread = threading.Thread(target=sync_loop, daemon=True)
    thread.start()

    yield  # à¹à¸­à¸›à¸à¸£à¹‰à¸­à¸¡à¹ƒà¸«à¹‰à¸šà¸£à¸´à¸à¸²à¸£

    print("ğŸ‘‹ Shutting down background Dropbox sensor sync...")
    stop_flag["stop"] = True
    time.sleep(1)


